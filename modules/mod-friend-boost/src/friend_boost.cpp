    player->SetSkill(SKILL_RIDING, 0, 0, 0);
    if (player->GetLevel() >= sPlayerbotAIConfig.useGroundMountAtMinLevel)
        player->learnSpell(33388);
    if (player->GetLevel() >= sPlayerbotAIConfig.useFastGroundMountAtMinLevel)
        player->learnSpell(33391);
