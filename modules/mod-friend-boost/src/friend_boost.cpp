constexpr uint32 CatchupBagItemId = 4245;
constexpr uint8 CatchupBagSlots = 10;
constexpr uint8 GearReplacementLevelGap = 5;

struct ProtectedEquipment
{
    uint8 Slot;
    ObjectGuid Guid;
};

uint32 GetCatchupItemLevel(ItemTemplate const* itemTemplate)
{
    if (!itemTemplate)
        return 0;

    if (itemTemplate->RequiredLevel > 0)
        return itemTemplate->RequiredLevel;

    return itemTemplate->ItemLevel;
}

bool IsVeryOutdatedForBoost(Item* item, uint8 targetLevel)
{
    if (!item)
        return true;

    uint32 const threshold = targetLevel > GearReplacementLevelGap
        ? targetLevel - GearReplacementLevelGap : 0;

    return GetCatchupItemLevel(item->GetTemplate()) <= threshold;
}

std::vector<ProtectedEquipment> SnapshotProtectedEquipment(
    Player* player, uint8 targetLevel)
{
    std::vector<ProtectedEquipment> protectedEquipment;

    for (uint8 slot = EQUIPMENT_SLOT_START; slot < EQUIPMENT_SLOT_END; ++slot)
    {
        if (slot == EQUIPMENT_SLOT_BODY || slot == EQUIPMENT_SLOT_TABARD)
            continue;

        Item* item = player->GetItemByPos(INVENTORY_SLOT_BAG_0, slot);
        if (item && !IsVeryOutdatedForBoost(item, targetLevel))
            protectedEquipment.push_back({ slot, item->GetGUID() });
    }

    return protectedEquipment;
}

void DestroyEquippedItem(Player* player, uint8 slot)
{
    Item* item = player->GetItemByPos(INVENTORY_SLOT_BAG_0, slot);
    if (!item)
        return;

    player->DestroyItem(INVENTORY_SLOT_BAG_0, slot, true);
}

void RestoreProtectedEquipment(
    Player* player, std::vector<ProtectedEquipment> const& protectedEquipment)
{
    for (ProtectedEquipment const& protectedItem : protectedEquipment)
    {
        Item* equipped = player->GetItemByPos(
            INVENTORY_SLOT_BAG_0, protectedItem.Slot);
        if (equipped && equipped->GetGUID() == protectedItem.Guid)
            continue;

        Item* original = player->GetItemByGuid(protectedItem.Guid);
        if (!original)
            continue;

        uint16 destination = 0;
        if (player->CanEquipItem(protectedItem.Slot, destination, original,
            true) != EQUIP_ERR_OK)
            continue;

        // The replacement was just generated by the boost. Throw it away and
        // put the player's still-good item back on.
        DestroyEquippedItem(player, protectedItem.Slot);

        if (player->CanEquipItem(protectedItem.Slot, destination, original,
            false) != EQUIP_ERR_OK)
            continue;

        player->RemoveItem(original->GetBagSlot(), original->GetSlot(), true);
        player->EquipItem(destination, original, true);
    }
}

bool IsBagEmpty(Bag const* bag)
{
    if (!bag)
        return true;

    for (uint8 slot = 0; slot < bag->GetBagSize(); ++slot)
        if (bag->GetItemByPos(slot))
            return false;

    return true;
}

void InitCatchupBags(Player* player)
{
    if (!sObjectMgr->GetItemTemplate(CatchupBagItemId))
        return;

    for (uint8 slot = INVENTORY_SLOT_BAG_START;
        slot < INVENTORY_SLOT_BAG_END; ++slot)
    {
        Item* oldItem = player->GetItemByPos(INVENTORY_SLOT_BAG_0, slot);
        if (oldItem)
        {
            Bag* oldBag = oldItem->ToBag();
            if (!oldBag || oldBag->GetBagSize() >= CatchupBagSlots ||
                !IsBagEmpty(oldBag))
                continue;

            player->DestroyItem(INVENTORY_SLOT_BAG_0, slot, true);
        }

        uint16 destination = 0;
        if (player->CanEquipNewItem(slot, destination, CatchupBagItemId,
            false) == EQUIP_ERR_OK)
            player->EquipNewItem(destination, CatchupBagItemId, true);
    }
}

void SetBoostSkill(Player* player, uint16 skillId, uint8 targetLevel)
{
    uint32 const maxValue = targetLevel * 5;
    uint16 const step = player->GetSkillValue(skillId)
        ? player->GetSkillStep(skillId) : 1;

    player->SetSkill(skillId, step, maxValue, maxValue);
}

